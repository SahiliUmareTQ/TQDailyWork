package com.stringdemo;

public class Compaire {

	public static void main(String[] args) {

		String s1 = "sahili";

		String s2 = "umare";

		System.out.println(s1.compareTo(s2));
		// s1 is greater ---> positive
		// s1 is smaller ---> negative

		String s3 = "Hello";

		String s4 = "World";

		System.out.println(s3.compareTo(s4));

	}

}
